package com.project.sections;

public class Variables {
    public static void main(String[] args) {
        int myAge = 30;
        int herAge = 35; // changing the value of the age variable
        String name = "Diogo";
        char oneCharacter = 'A';
        System.out.println(myAge + " " + oneCharacter + " " + name); // Output is 30 A Diogo ;
        System.out.println(2 + herAge); // Output is 65 ;
        
    }
}

