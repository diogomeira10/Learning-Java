package com.project.sections.ControlFlow;

public class Loops {
    public static void main(String[] args) {

        //basic for loop structure
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }

        //while loops
        int i = 0;
        while (i < 5) {
            System.out.println("Hello World");
            i++;
        }
    }
}
